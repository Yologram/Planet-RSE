    <h2 class="ui dividing header">Pourquoi la RSE ?</h2>
  </div>
  <div class="ui nine wide left aligned column">
    <p>D’une part, l’amélioration de la qualité environnementale, sociale et économique du territoire se situe au cœur des préoccupations de la collectivité. D’autre part certaines entreprises, affaiblies par la situation économique depuis 2008, perçoivent dans la RSE un moyen de différenciation autre que le prix : plutôt que d’être sélectionnées sur le seul critère de prix (devenant dangereusement bas lorsque l’offre de biens et de services est supérieure à la demande), d’autres indicateurs valorisés par les acheteurs publics permettent à l’entreprise de se différencier tout en entrant dans un processus d’amélioration continue en matière de RSE.<br />
      Un objectif commun est donc né de ces échanges, celui d’inoculer la RSE au sein de la commande publique ainsi que dans les entreprises et d’amorcer une collaboration étroite entre public et privé afin d’élaborer des outils de mesure de la RSE qui soient pertinents et qui permettent de mener une comparaison objective des différents acteurs d’un secteur donné.</p>
    </div>
  </section>

