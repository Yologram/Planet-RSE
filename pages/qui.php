<h1 class="ui dividing header">Qui est évalué ?</h1>
</br>

<p>L’association audite les TPE, les PME et les institutions du territoire qui souhaitent mesurer précisément le niveau d’intégration de la RSE au sein de leurs structures. Ces audits permettent d’obtenir une analyse objective et quantifiée des démarches initiées en terme de responsabilité sociétale.</p>

<p>La RSE est évaluée au niveau de la plus petite entité qui répond à un appel d’offre. Ainsi, dans le cas d’une grande entreprise répartie en filiales, antennes ou franchises, seules les données de l’établissement en charge de l’exécution du marché doivent être renseignées. Cela permet d’éviter qu’une petite entreprise exerçant une activité pour laquelle le taux d’accidents du travail est élevé soit mise en comparaison avec un grand groupe dont le nombre total de salariés « dilue » la part des emplois à risque.</p>

<p>A titre d’exemple, le taux d’accidents du travail d’une petite PME du secteur du bâtiment dont 80% des effectifs sont sur le terrain ne peut pas être comparée aux taux d’accidents du travail d’une grande entreprise dont 80% des salariés exercent une fonction de bureau et dont seulement 20% de l’activité du groupe concerne le secteur du bâtiment.</p>
